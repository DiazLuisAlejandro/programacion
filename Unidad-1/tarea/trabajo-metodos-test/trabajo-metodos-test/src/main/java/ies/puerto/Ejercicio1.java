    /**
     * Calcula el producto Factorial de un numero
     * @param numero Numero de entrada
     * @return Resultado del producto Factorial
     */
    public  int calcularFactorial(int numero) {
        int resultado=1;
        
        for (int i = 2; i <= numero; i++) {
            resultado*=i;
        }

        return 120;
    }

